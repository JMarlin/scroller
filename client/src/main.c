int handleEvent(Event* event) {
    if(event->type == KEY) {
        KeyEvent* keyEvent = (KeyEvent*)event;
        //printf("Key event: %d\n", ((KeyEvent*)event)->code);
        y_velo =
            keyEvent->code == 38
                ? keyEvent->isUp ? 0 : -1
                : keyEvent->code == 40
                    ? keyEvent->isUp
                        ? 0
                        : 1
                    : y_velo;
        x_velo =
            keyEvent->code == 37
                ? keyEvent->isUp ? 0 : -1
                : keyEvent->code == 39
                    ? keyEvent->isUp
                        ? 0
                        : 1
                    : x_velo;
    }

    if(event->type == MOUSE) {
        MouseEvent* mouseEvent = (MouseEvent*)event;
        pos_x = mouseEvent->x;
        pos_y = mouseEvent->y;
    }

    return 0;
}

